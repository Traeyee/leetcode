class Solution {
   public:
    // 双最小堆版本
    int largestRectangleArea__FALSE(vector<int>& heights) {
        priority_queue<int, vector<int>, greater<int>> min_heap, pending_heap;
        for (int h : heights) {
            min_heap.push(h);
        }

        int n = heights.size();
        int l = 0, r = n - 1;
        int max_area = 0;
        while (l <= r) {
            int width = r - l + 1;
            int min_h = min_heap.top();
            cout << "# " << l << ", " << r << ": " << min_h << "\n";
            int area = width * min_h;
            max_area = area > max_area ? area : max_area;

            if (heights[l] < heights[r]) {
                if (heights[l] == min_heap.top()) {
                    min_heap.pop();
                } else {
                    pending_heap.push(heights[l]);
                }
                l++;
            } else {
                if (heights[r] == min_heap.top()) {
                    min_heap.pop();
                } else {
                    pending_heap.push(heights[r]);
                }
                r--;
            }
            while (pending_heap.size() > 0 &&
                   min_heap.top() == pending_heap.top()) {
                min_heap.pop();
                pending_heap.pop();
            }
        }

        return max_area;
    }
    // 对空间进行优化，但还是TL了，得对时间再进行优化
    int largestRectangleArea__BASELINE2(vector<int>& heights) {
        int n = heights.size();

        int max_area = 0;
        for (int i = 0; i < n; i++) {
            int min_len = heights[i];
            max_area = min_len > max_area ? min_len : max_area;
            for (int j = i + 1; j < n; j++) {
                min_len = heights[j] < min_len ? heights[j] : min_len;
                int width = j - i + 1;
                int area = width * min_len;
                max_area = area > max_area ? area : max_area;
            }
        }

        return max_area;
    }
    // 栈溢出，必须得优化
    int largestRectangleArea__BASELINE(vector<int>& heights) {
        int n = heights.size();
        int min_len[n][n];  // min_len[i][j]: 区间i-j的最小值

        int max_area = 0;
        for (int i = 0; i < n; i++) {
            min_len[i][i] = heights[i];
            max_area = min_len[i][i] > max_area ? min_len[i][i] : max_area;
            for (int j = i + 1; j < n; j++) {
                min_len[i][j] = heights[j] < min_len[i][j - 1]
                                    ? heights[j]
                                    : min_len[i][j - 1];
                int width = j - i + 1;
                int area = width * min_len[i][j];
                max_area = area > max_area ? area : max_area;
            }
        }

        return max_area;
    }
};

